    private String[] separationsOfTextField() {
        String str = separateSemicolon();
        String[] array = separateCommas(str);
        return array;
    }

 
    private String separateSemicolon() {
        String listOfNumbers = this.jTextField1.getText();
        String[] firstArray = listOfNumbers.split(";");
        StringBuilder builder = new StringBuilder();
        for (String s : firstArray) {
            builder.append(s).append(",");
        }
        String str = builder.toString();
        return str;
    }

    private String[] separateCommas(String str) {
        String[] array = str.split(",");
        return array;
    }

    private double[] initializeArrayOfAllTheNumbers(String[] array) {
        double[] numbers = new double[array.length];
        return numbers;
    }

    private double[] initializeCoordenates(double[] numbers) {
        double array[] = new double[numbers.length / 2];
        return array;
    }

    private void sumOfXsAndYs(double[] numbers, double[] arrayX, double[] arrayY) {
        double sumX = 0, sumY = 0;
        int n = numbers.length / 2;
        for (int i = 0; i < n; i++) {
            sumX = sumX + arrayX[i];
            sumY = sumY + arrayY[i];
        }
    }

    private void separateXsandYs(String[] array, double[] numbers, double[] arrayX, double[] arrayY) throws NumberFormatException {
        int contX = 0, contY = 0;
        for (int i = 0; i < array.length; i++) {
            numbers[i] = Integer.parseInt(array[i]);
        }
        for (int i = 0; i < numbers.length; i++) {
            if (i % 2 == 0) {
                arrayX[contX] = numbers[i];
                contX++;
            } else {
                arrayY[contY] = numbers[i];
                contY++;
            }
        }
    }

    private double getPearsonsCorrelation(double[] arrayX, double[] arrayY) {
        //105,4;116,8;103,2;124,7;137,9;126,9;112,3;129,10;118,7;105,6
        PearsonsCorrelation p = new PearsonsCorrelation();
        return p.correlation(arrayX, arrayY);
    }

    private String getPearsonsCorrelationString(double[] arrayX, double[] arrayY) {
        //105,4;116,8;103,2;124,7;137,9;126,9;112,3;129,10;118,7;105,6
        PearsonsCorrelation p = new PearsonsCorrelation();
        String result = String.valueOf(p.correlation(arrayX, arrayY));
        return result;
    }

    private void setResultInLabel(double[] arrayX, double[] arrayY) {
        double resultOfCorrelation = getPearsonsCorrelation(arrayX, arrayY);
        SerachForTypeOfRelationship(resultOfCorrelation, arrayX, arrayY);
    }

    private void SerachForTypeOfRelationship(double resultOfCorrelation, double[] arrayX, double[] arrayY) {
        if (resultOfCorrelation == 0 || (resultOfCorrelation > 0 && resultOfCorrelation < 1) || (resultOfCorrelation < 0 && resultOfCorrelation > -1)) {
            this.jLabel1.setText(getPearsonsCorrelationString(arrayX, arrayY) + " -> Debil o nula relacion");
        } else if (resultOfCorrelation > 1) {
            this.jLabel1.setText(getPearsonsCorrelationString(arrayX, arrayY) + " -> Fuerte relacion positiva");
        } else if (resultOfCorrelation < -1) {
            this.jLabel1.setText(getPearsonsCorrelationString(arrayX, arrayY) + " -> Fuerte relacion negativa");
        }
    }


